    public static int wielkoscTabeli(DanePodstawowe tab[], int index1d) {

        int index2d = 0;


        for (int i = 0; i < tab.length; i++) {

            if (tab[i] != null) {

                if (tab[i].odlegloscOdRadaru() < Math.pow(3, index1d)) {

                    index2d++;

                }
            }
        }
        return index2d;
    }

    public static DanePodstawowe[][] sortuj(DanePodstawowe[][] posortowanaTabela2d, DanePodstawowe[] tab) {

        int index=0;

        for (int i = 0; i < tab.length; i++) {

            int odleglosc = tab[i].odlegloscOdRadaru();

            while (odleglosc / 3 > 0) {

                odleglosc /= 3;
                index++;

            }
            if (posortowanaTabela2d[index].length != 0) {
                    int index2d = 0;
                    while (posortowanaTabela2d[index][index2d] != null) {

                        index2d++;

                    }

                posortowanaTabela2d[index][index2d] = tab[i];

            }
            index=0;
        }

        return posortowanaTabela2d;
    }

    public static DanePodstawowe[] sort1d(DanePodstawowe[] arr){
        for (int j = 0; j < arr.length; j++) {
            for (int i = 0; i < arr.length; i++) {

                if (arr[j].odlegloscOdRadaru() < arr[i].odlegloscOdRadaru()){
                    DanePodstawowe tmp = arr[j];
                    arr[j]=arr[i];
                    arr[i]=tmp;
                }
            }
        }

        return arr;
    }

